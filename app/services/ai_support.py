"""
AI Поддержка - чат-бот и помощь пользователям
"""
from datetime import datetime


class AISupport:
    """ИИ-поддержка для помощи пользователям."""
    
    # База знаний (FAQ)
    KNOWLEDGE_BASE = {
        "регистрация": {
            "keywords": ["регистрация", "зарегистрироваться", "создать аккаунт", "sign up"],
            "answer": "Для регистрации перейдите на /auth/register и укажите ФИО, телефон и пароль. Выберите роль: shipper (грузоотправитель) или carrier (перевозчик)."
        },
        "заявка": {
            "keywords": ["создать заявку", "добавить груз", "разместить груз", "новая заявка"],
            "answer": "Чтобы создать заявку, используйте /loads/create. Укажите город отправления, город назначения, вес, объём и цену."
        },
        "ставка": {
            "keywords": ["сделать ставку", "откликнуться", "bid", "предложить цену"],
            "answer": "Чтобы сделать ставку на заявку, используйте /bids/{load_id}/add. Укажите вашу цену и комментарий."
        },
        "оплата": {
            "keywords": ["оплата", "платёж", "деньги", "оплатить"],
            "answer": "Оплата происходит напрямую между заказчиком и перевозчиком после выполнения заказа. Платформа не удерживает средства."
        },
        "спор": {
            "keywords": ["спор", "проблема", "жалоба", "конфликт", "обман"],
            "answer": "В случае спора обратитесь в поддержку. Наш ИИ-юрист проанализирует ситуацию и предложит решение. Сохраняйте всю переписку."
        },
        "рейтинг": {
            "keywords": ["рейтинг", "отзыв", "оценка", "репутация"],
            "answer": "Рейтинг формируется на основе выполненных заказов и отзывов. Высокий рейтинг повышает доверие к вам."
        },
        "договор": {
            "keywords": ["договор", "контракт", "документы", "оформление"],
            "answer": "Договор генерируется автоматически после принятия ставки. Вы получите готовый документ с реквизитами сторон."
        }
    }
    
    # Приветствия
    GREETINGS = ["привет", "здравствуй", "добрый день", "hello", "hi", "хай"]
    
    def get_response(self, message: str) -> dict:
        """Получить ответ на сообщение пользователя."""
        
        message_lower = message.lower().strip()
        
        # Проверка на приветствие
        if any(greet in message_lower for greet in self.GREETINGS):
            return {
                "type": "greeting",
                "message": "Здравствуйте! Я ИИ-помощник ГрузПоток. Чем могу помочь?",
                "suggestions": ["Как создать заявку?", "Как сделать ставку?", "Как работает оплата?"]
            }
        
        # Поиск в базе знаний
        for topic, data in self.KNOWLEDGE_BASE.items():
            if any(keyword in message_lower for keyword in data["keywords"]):
                return {
                    "type": "answer",
                    "topic": topic,
                    "message": data["answer"],
                    "suggestions": self._get_related_topics(topic)
                }
        
        # Ответ по умолчанию
        return {
            "type": "fallback",
            "message": "Извините, я не совсем понял вопрос. Попробуйте переформулировать или выберите тему:",
            "suggestions": list(self.KNOWLEDGE_BASE.keys())
        }
    
    def _get_related_topics(self, current_topic: str) -> list:
        """Получить связанные темы."""
        related = {
            "регистрация": ["заявка", "ставка"],
            "заявка": ["ставка", "договор"],
            "ставка": ["оплата", "договор"],
            "оплата": ["спор", "договор"],
            "спор": ["рейтинг", "оплата"],
            "рейтинг": ["регистрация", "заявка"],
            "договор": ["оплата", "спор"]
        }
        return related.get(current_topic, ["регистрация", "заявка"])
    
    def handle_complaint(self, user_id: int, complaint_text: str) -> dict:
        """Обработка жалобы пользователя."""
        
        ticket_id = f"TKT-{datetime.now().strftime('%Y%m%d%H%M%S')}"
        
        # Анализ срочности
        urgent_keywords = ["срочно", "обман", "мошенник", "украли", "кража"]
        is_urgent = any(word in complaint_text.lower() for word in urgent_keywords)
        
        return {
            "ticket_id": ticket_id,
            "status": "created",
            "priority": "high" if is_urgent else "normal",
            "message": f"Ваша жалоба зарегистрирована под номером {ticket_id}. "
                      f"{'Мы рассмотрим её в приоритетном порядке.' if is_urgent else 'Мы рассмотрим её в течение 24 часов.'}",
            "next_steps": [
                "Сохраните номер обращения",
                "Подготовьте скриншоты переписки",
                "Ожидайте ответа от модератора"
            ]
        }
    
    def get_help_menu(self) -> dict:
        """Получить меню помощи."""
        return {
            "title": "Справочный центр ГрузПоток",
            "sections": [
                {
                    "name": "Начало работы",
                    "topics": ["регистрация", "заявка"]
                },
                {
                    "name": "Перевозки",
                    "topics": ["ставка", "договор", "оплата"]
                },
                {
                    "name": "Безопасность",
                    "topics": ["рейтинг", "спор"]
                }
            ],
            "contact": "Для связи с поддержкой используйте /support/complaint"
        }


# Singleton instance
ai_support = AISupport()



