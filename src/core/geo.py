def _normalize_city_key(name: str) -> str:
    t = (name or "").strip().lower()
    t = t.replace("ё", "е").replace("-", " ")
    t = re.sub(r"[^0-9a-zа-я\s]", " ", t)
    return re.sub(r"\s+", " ", t).strip()


def city_coords(city_name: str) -> tuple[float, float] | None:
    """Return (lat, lon) for a city name, or None if unknown."""
    return CITY_COORDS.get(_normalize_city_key(city_name))


def resolve_region(name: str) -> list[str] | None:
    """If name is a region alias, return the list of cities in that region."""
    key = _normalize_city_key(name)
    region = REGION_ALIASES.get(key)
    if region:
        return REGION_CLUSTERS[region]
    return None


def region_center(name: str) -> tuple[float, float] | None:
    """Return the centroid (avg lat, avg lon) of a region's cities."""
    cities = resolve_region(name)
    if not cities:
        return None
    coords = [CITY_COORDS[c] for c in cities if c in CITY_COORDS]
    if not coords:
        return None
    avg_lat = sum(c[0] for c in coords) / len(coords)
    avg_lon = sum(c[1] for c in coords) / len(coords)
    return (avg_lat, avg_lon)


def haversine_km(
    lat1: float, lon1: float, lat2: float, lon2: float
) -> float:
    """Great-circle distance between two points in km."""
    r = 6371.0
    dlat = math.radians(lat2 - lat1)
    dlon = math.radians(lon2 - lon1)
    a = (
        math.sin(dlat / 2) ** 2
        + math.cos(math.radians(lat1))
        * math.cos(math.radians(lat2))
        * math.sin(dlon / 2) ** 2
    )
    return 2 * r * math.asin(math.sqrt(a))



